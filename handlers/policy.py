from aiogram import Router
from aiogram.types import Message
from aiogram.filters import Command

router = Router(name="policy")


POLICY_TEXT = """<b>Как используются ваши данные</b>

- <b>Открытый датасет.</b> Всё, что вы отправляете, войдёт в открытую базу бурятского языка для проекта Mozilla Common Voice и подобных инициатив.

- <b>Свободная лицензия (CC0).</b> Тексты и записи публикуются в общественном достоянии — их сможет свободно использовать кто угодно, включая коммерческие проекты.

- <b>Фундамент для технологий.</b> Данные станут основой для распознавания и синтеза бурятской речи: голосовых помощников, субтитров, приложений для изучения языка.

- <b>Только ваши тексты.</b> Присылайте собственные фразы или тексты из общественного достояния — не копируйте из книг, песен и статей.

- <b>Голос — добровольно.</b> Отправляя запись, вы соглашаетесь на её открытую публикацию. После публикации под CC0 отозвать её нельзя.

- <b>Без личных данных.</b> Мы не собираем ничего, кроме самих фраз и записей голоса. Участие — с 18 лет."""


@router.message(Command("policy"))
async def policy_message(message: Message) -> None:
    await message.answer(POLICY_TEXT)