from aiogram import Router
from aiogram.types import Message
from aiogram.filters import Command

router = Router(name="policy")


POLICY_TEXT = """<b>Как мы используем ваши данные</b>

- <b>Открытое собрание фраз.</b> Всё, что вы присылаете, войдёт в открытую базу бурятского языка для проекта Common Voice и похожих начинаний.

- <b>Свободное использование.</b> Тексты и записи будут доступны всем — в том числе для учёбы и полезных программ.

- <b>Для технологий языка.</b> На этих данных учат программы понимать и озвучивать бурятскую речь: помощники, субтитры, приложения для изучения языка.

- <b>Только свои тексты.</b> Присылайте свои фразы или то, что уже свободно для всех. Не копируйте из книг, песен и статей.

- <b>Голос — по желанию.</b> Отправляя запись, вы соглашаетесь на её открытую публикацию. Потом отозвать её нельзя.

- <b>Без личных сведений.</b> Мы не собираем ничего, кроме самих фраз и записей голоса. Участие — с 18 лет."""

@router.message(Command("policy"))
async def policy_message(message: Message) -> None:
    await message.answer(POLICY_TEXT)